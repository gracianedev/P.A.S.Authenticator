private void redimensionarImagem(JLabel label, ImageIcon imagemOriginal) {
    if (imagemOriginal != null) {
        int largura = label.getWidth();
        int altura = label.getHeight();
        if (largura > 0 && altura > 0) {
            Image imagemRedimensionada = imagemOriginal.getImage().getScaledInstance(
                    largura, altura, Image.SCALE_SMOOTH);
            label.setIcon(new ImageIcon(imagemRedimensionada));
        }
    }
}




